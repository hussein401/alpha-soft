<script>
    document.addEventListener('DOMContentLoaded', () => {
        const embeddedForm = document.getElementById('embedded-chat-form');
        const embeddedInput = document.getElementById('embedded-chat-input');
        const embeddedMessages = document.getElementById('embedded-chat-messages');
        const embeddedHistory = [];

        if (embeddedForm) {
            embeddedForm.addEventListener('submit', async (e) => {
                e.preventDefault();
                const msg = embeddedInput.value.trim();
                if (!msg) return;
                
                sendMessage(msg, embeddedInput, embeddedMessages, embeddedHistory);
            });
        }

        window.sendQuickReply = (text) => {
            const input = document.getElementById('embedded-chat-input');
            const messages = document.getElementById('embedded-chat-messages');
            sendMessage(text, input, messages, embeddedHistory);
        };
    });

    async function sendMessage(msg, input, container, history) {
        addEmbeddedMessage(msg, 'user', container);
        input.value = '';

        try {
            const response = await fetch('<?php echo e(route("chat")); ?>', {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json',
                    'X-CSRF-TOKEN': '<?php echo e(csrf_token()); ?>'
                },
                body: JSON.stringify({ message: msg, history: history })
            });

            const data = await response.json();
            if (data.reply) {
                addEmbeddedMessage(data.reply, 'bot', container);
                history.push({ role: 'user', content: msg });
                history.push({ role: 'bot', content: data.reply });
            } else {
                addEmbeddedMessage("I'm having trouble connecting right now.", 'bot', container);
            }
        } catch (error) {
            addEmbeddedMessage("Error connecting to server.", 'bot', container);
        }
    }

    function addEmbeddedMessage(text, role, container) {
        const div = document.createElement('div');
        div.style.display = 'flex';
        div.style.gap = '0.75rem';
        div.style.marginBottom = '1rem';
        if (role === 'user') div.style.flexDirection = 'row-reverse';

        const avatar = document.createElement('div');
        avatar.style.width = '32px';
        avatar.style.height = '32px';
        avatar.style.borderRadius = '50%';
        avatar.style.display = 'flex';
        avatar.style.alignItems = 'center';
        avatar.style.justifyContent = 'center';
        avatar.style.flexShrink = '0';

        if (role === 'user') {
            avatar.style.background = 'rgba(255,255,255,0.1)';
            avatar.innerHTML = '<i class="fa-solid fa-user" style="font-size: 0.9rem; color: white;"></i>';
        } else {
            avatar.style.background = 'linear-gradient(135deg, var(--primary-cyan), var(--primary-blue))';
            avatar.innerHTML = '<i class="fa-solid fa-robot text-white" style="font-size: 0.9rem;"></i>';
        }

        const bubble = document.createElement('div');
        bubble.className = `embedded-msg-bubble ${role}`;
        
        // Basic markdown-like bold parsing
        let html = text.replace(/\*\*([^*]+)\*\*/g, '<strong>$1</strong>');
        bubble.innerHTML = html;

        div.appendChild(avatar);
        div.appendChild(bubble);
        container.appendChild(div);
        container.scrollTop = container.scrollHeight;
    }
</script>
